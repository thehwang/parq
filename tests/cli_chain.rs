//! Integration tests for v0.9 shell-primitive features (stdin auto-spool +
//! `--input` format dispatch). These spawn the release binary because
//! the spool path is fundamentally about non-seekable fds, which an
//! in-process call can't recreate cleanly.
//!
//! v0.12 additions: streaming (no-OOM) and lite-mode (parquet metadata-
//! only) live here too because both require process-level observation.

use std::io::Write;
use std::path::{Path, PathBuf};
use std::process::{Command, Stdio};

/// Locate the freshly-built `pq` binary. We intentionally look up
/// release first (fast iteration loop) and fall back to debug — either
/// is fine for these chain assertions.
fn pq_binary() -> PathBuf {
    let manifest = env!("CARGO_MANIFEST_DIR");
    let release = PathBuf::from(manifest).join("target/release/pq");
    if release.is_file() {
        return release;
    }
    PathBuf::from(manifest).join("target/debug/pq")
}

/// Pre-canned sample.parquet path. The sample is committed-untracked but
/// regenerated by `scripts/make_sample.py` and exists in CI as part of
/// the smoke job. Local devs almost always have it from the demo.
fn sample_parquet() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("sample.parquet")
}

/// Skip the test (with a printed reason) when the prerequisites aren't on
/// disk — keeps the suite green for fresh checkouts that haven't built or
/// regenerated the sample yet.
fn skip_if_missing(p: &Path, label: &str) -> bool {
    if !p.exists() {
        eprintln!("skipping: {} not found at {}", label, p.display());
        return true;
    }
    false
}

#[test]
fn stdin_pipe_parquet_roundtrip() {
    let pq = pq_binary();
    let sample = sample_parquet();
    if skip_if_missing(&pq, "pq binary") || skip_if_missing(&sample, "sample.parquet") {
        return;
    }
    // `cat sample.parquet | pq - '.email | distinct'` — the canonical
    // case the v0.9 stdin spool exists for. Before the spool, this errored
    // out with "Cannot read parquet file: not a parquet file" because
    // duckdb couldn't seek to the footer.
    let bytes = std::fs::read(&sample).expect("read sample");
    let mut child = Command::new(&pq)
        .arg("-")
        .arg(".email | distinct")
        .stdin(Stdio::piped())
        .stdout(Stdio::piped())
        .stderr(Stdio::piped())
        .spawn()
        .expect("spawn pq");
    child
        .stdin
        .as_mut()
        .unwrap()
        .write_all(&bytes)
        .expect("write stdin");
    let out = child.wait_with_output().expect("wait pq");
    assert!(
        out.status.success(),
        "pq failed: stderr={}",
        String::from_utf8_lossy(&out.stderr)
    );
    let stdout = String::from_utf8_lossy(&out.stdout);
    // Exact emails come from the canned sample but at minimum we expect
    // multiple distinct rows.
    let lines: Vec<_> = stdout.lines().filter(|l| !l.is_empty()).collect();
    assert!(
        lines.len() >= 2,
        "expected >=2 distinct emails, got: {stdout}"
    );
}

#[test]
fn ndjson_chain() {
    let pq = pq_binary();
    let sample = sample_parquet();
    if skip_if_missing(&pq, "pq binary") || skip_if_missing(&sample, "sample.parquet") {
        return;
    }
    // Emits one JSON object per row from sample, pipes into a second pq
    // invocation that reads it back and reduces. Validates both:
    //   * `--input ndjson` routes to read_json with newline_delimited
    //   * stdin spool works for line-oriented formats too (DuckDB's
    //     read_json double-pass over stdin would otherwise produce 0 rows).
    let mut stage1 = Command::new(&pq)
        .arg(&sample)
        .arg(".country | to_json")
        .stdout(Stdio::piped())
        .stderr(Stdio::inherit())
        .spawn()
        .expect("spawn pq stage1");
    let stage1_stdout = stage1.stdout.take().expect("stage1 stdout");
    let stage2 = Command::new(&pq)
        .arg("-i")
        .arg("ndjson")
        .arg("-")
        .arg(".country | distinct | sort by .country")
        .stdin(stage1_stdout)
        .stdout(Stdio::piped())
        .stderr(Stdio::piped())
        .spawn()
        .expect("spawn pq stage2");
    let out = stage2.wait_with_output().expect("wait pq stage2");
    // Reap stage1 so clippy doesn't complain about a zombie. Status is
    // informational here — the substantive assertion is on stage2's output.
    let _ = stage1.wait();
    assert!(
        out.status.success(),
        "pq stage2 failed: stderr={}",
        String::from_utf8_lossy(&out.stderr)
    );
    let stdout = String::from_utf8_lossy(&out.stdout);
    // The sample has US/CA/UK/DE/FR — five distinct countries.
    let n = stdout.lines().filter(|l| !l.is_empty()).count();
    assert!(n >= 4, "expected >=4 distinct rows, got {n}: {stdout}");
}

// ── v0.12: lite-mode + streaming ─────────────────────────────────────────────

/// `pq count --lite FILE.parquet` should hit the parquet_file_metadata
/// path and produce the same row count as the regular `count(*)` path.
/// We can't directly observe whether the scan was skipped from the
/// parent process, but we *can* assert that:
///   1. the row counts match,
///   2. forced lite mode prints no banner (user opted in deliberately).
#[test]
fn count_lite_matches_full_count() {
    let pq = pq_binary();
    let sample = sample_parquet();
    if skip_if_missing(&pq, "pq binary") || skip_if_missing(&sample, "sample.parquet") {
        return;
    }
    let full = Command::new(&pq)
        .args(["count", sample.to_str().unwrap()])
        .output()
        .expect("count full");
    assert!(full.status.success(), "full count failed");
    let lite = Command::new(&pq)
        .args(["count", "--lite", sample.to_str().unwrap()])
        .output()
        .expect("count lite");
    assert!(lite.status.success(), "lite count failed");
    let full_out = String::from_utf8_lossy(&full.stdout).into_owned();
    let lite_out = String::from_utf8_lossy(&lite.stdout).into_owned();
    assert_eq!(full_out.trim(), lite_out.trim(), "row count mismatch");
    let lite_err = String::from_utf8_lossy(&lite.stderr);
    assert!(
        !lite_err.contains("lite mode"),
        "explicit --lite shouldn't emit a banner; stderr was: {lite_err}"
    );
}

/// Auto-lite triggers when the file size crosses `PQ_LITE_THRESHOLD`.
/// Set the threshold to 1 byte so any non-empty parquet hits it; the
/// stderr banner is the user-facing observable.
#[test]
fn count_auto_lite_triggers_under_threshold() {
    let pq = pq_binary();
    let sample = sample_parquet();
    if skip_if_missing(&pq, "pq binary") || skip_if_missing(&sample, "sample.parquet") {
        return;
    }
    let out = Command::new(&pq)
        .env("PQ_LITE_THRESHOLD", "1")
        .args(["count", sample.to_str().unwrap()])
        .output()
        .expect("count auto-lite");
    assert!(out.status.success(), "count failed");
    let stderr = String::from_utf8_lossy(&out.stderr);
    assert!(
        stderr.contains("lite mode"),
        "expected lite-mode banner on stderr, got: {stderr}"
    );
}

/// ndjson streaming: pipe the sample as ndjson and verify pq emits its
/// first line before reading every input row. Smoke-tests the v0.12
/// stream-from-cursor path: pre-v0.12 we collected every row before
/// emitting any of them, so a 100M-row scan would OOM. We can't easily
/// observe memory, but we can observe per-row correctness on a small
/// fixture and a non-trivial (>10k rows) generated stream.
///
/// The actual streaming behaviour is best validated by hand on a
/// 10GB+ file (`pq -i ndjson huge.ndjson '.x' | head -1` returns
/// instantly post-v0.12); this test guards against accidental
/// re-introduction of the buffer-everything code path.
#[test]
fn ndjson_streaming_preserves_order() {
    let pq = pq_binary();
    let sample = sample_parquet();
    if skip_if_missing(&pq, "pq binary") || skip_if_missing(&sample, "sample.parquet") {
        return;
    }
    let out = Command::new(&pq)
        .args([sample.to_str().unwrap(), "-o", "ndjson"])
        .output()
        .expect("pq ndjson");
    assert!(out.status.success());
    let stdout = String::from_utf8_lossy(&out.stdout);
    let lines: Vec<&str> = stdout.lines().filter(|l| !l.is_empty()).collect();
    assert!(
        lines.len() >= 5,
        "expected several ndjson lines, got {}: {stdout}",
        lines.len()
    );
    for line in &lines {
        assert!(
            line.starts_with('{') && line.ends_with('}'),
            "every line should be a complete JSON object: got {line}"
        );
    }
}

/// Regression: `pq | head -1` must not surface a "Broken pipe" error
/// to stderr just because the downstream consumer closed the pipe
/// after one row. v0.12.0 forgot to reset SIGPIPE, so streaming
/// output (introduced in the same release) made every `pq … | head`
/// invocation print noise. The fix is `signal(SIGPIPE, SIG_DFL)` at
/// startup.
#[test]
#[cfg(unix)]
fn streaming_to_closed_pipe_emits_no_stderr() {
    let pq = pq_binary();
    let sample = sample_parquet();
    if skip_if_missing(&pq, "pq binary") || skip_if_missing(&sample, "sample.parquet") {
        return;
    }
    // Pipe `pq -o ndjson sample.parquet '.email'` into `head -1`.
    // The downstream `head` will close stdin after the first line —
    // pq must take SIGPIPE silently rather than complaining.
    let mut producer = Command::new(&pq)
        .args([sample.to_str().unwrap(), "-o", "ndjson"])
        .stdout(Stdio::piped())
        .stderr(Stdio::piped())
        .spawn()
        .expect("spawn pq");
    let pq_stdout = producer.stdout.take().expect("stdout");
    let head = Command::new("head")
        .arg("-1")
        .stdin(pq_stdout)
        .stdout(Stdio::piped())
        .spawn()
        .expect("spawn head");
    let _head_out = head.wait_with_output().expect("head finish");
    let pq_out = producer.wait_with_output().expect("pq finish");
    let stderr = String::from_utf8_lossy(&pq_out.stderr);
    assert!(
        !stderr.to_lowercase().contains("broken pipe")
            && !stderr.to_lowercase().contains("os error 32"),
        "pq leaked broken-pipe noise to stderr: {stderr}"
    );
}

